SELECT MONTH(START_DATE) AS MONTH, CAR_ID, COUNT(*) AS RECORDS 
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY 
WHERE LEFT(START_DATE,7) IN ('2022-08','2022-09','2022-10') 
AND CAR_ID IN (SELECT CAR_ID FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY WHERE LEFT(START_DATE,7) IN ('2022-08','2022-09','2022-10') GROUP BY CAR_ID HAVING COUNT(*)>4)
GROUP BY CAR_ID, MONTH(START_DATE) HAVING RECORDS>0
ORDER BY MONTH ASC, CAR_ID DESC;